#如何在python中实现二叉树的各种遍历

class BinaryTree(object):
	"""docstring for BinaryTree"""
	def __init__(self, val):
		super(BinaryTree, self).__init__()
		self.val = val
		self.left = None
		self.right = None

	def __str__():
		print(self.val)



if __name__ == '__main__':
	tree = BinaryTree("value")
	print(tree)		